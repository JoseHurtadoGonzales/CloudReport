// Single source of truth for the active locale. State() makes the ref shared
// across all useI18n() callers in the same SSR request (Nuxt feature).
export function useI18n() {
  const cookie = useCookie<Locale>('cr-locale', {
    default: () => 'es',
    sameSite: 'lax',
    path: '/',
    maxAge: 60 * 60 * 24 * 365,
  })
  const locale = useState<Locale>('cr-locale', () => cookie.value || 'es')

  function setLocale(next: Locale) {
    locale.value = next
    cookie.value = next
    if (import.meta.client) {
      // Re-render anything that captured the locale via a closure. Components
      // that use t() inside the <template> are reactive automatically because
      // they read from `locale`.
      document.documentElement.lang = next
    }
  }

  function t(key: string): string {
    const active = messages[locale.value]
    if (active && active[key] != null) return active[key]
    const fallback = messages.es
    if (fallback && fallback[key] != null) return fallback[key]
    return key
  }

  return { locale, setLocale, t }
}
